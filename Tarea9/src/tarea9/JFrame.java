    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser(); 
        jfc.setDialogTitle("Buscar imagen");
        // Aplicamos la clase de Filtra Im√°genes para s√≥lo leer extensiones .png, .jpg, .jpeg, .gif
        jfc.addChoosableFileFilter(new FiltraImagenes());
        jfc.setAcceptAllFileFilterUsed(false);   
        // Checamos si el archivo seleccionado es una imagen y la mostramos en la aplicaci√≥n
        if(jfc.showOpenDialog(this) == JFileChooser.APPROVE_OPTION){
            archivo = new File(jfc.getSelectedFile().toString());
            rsscalelabel.RSScaleLabel.setScaleLabel(imagenCargada, archivo.toString());
            try{
                bfi = ImageIO.read(archivo);
                imagenSeleccionada = bfi;
            }catch(Exception e){}
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String texto_oculto = texto.getText();
        BufferedImage i;
        if(!texto_oculto.equals("")){
            Esteganografia e = new Esteganografia(bfi);
            i = e.ocultar(texto_oculto);
            imagenM = i;
            try{
                ImageIO.write(i, "bmp", new File("imagenConMensaje.bmp"));
                JOptionPane.showMessageDialog(null,"Se cre√≥ la imagen");
                rsscalelabel.RSScaleLabel.setScaleLabel(imagenModificada, "imagenConMensaje.bmp");
            }catch (IOException ioe) {
                System.out.println("Error de escritura");
                imagenM = null;
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "No se ha podido crear la imagen, por favor ingresa un texto");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

